package tamilnadu.chennai;

public class ArithmeticOperator {
		  public static void main(String[] args) {
		    
		    // declare variables
		    int a = 5, b = 12;

		    // addition operator
		    System.out.println("a + b = " + (a + b));

		    // subtraction operator
		    System.out.println("a - b = " + (a - b));

		    // multiplication operator
		    System.out.println("a * b = " + (a * b));

		    // division operator
		    System.out.println("a / b = " + (a / b));

		    // modulo operator
		    System.out.println("a % b = " + (a % b));
		  }
		}
